#En Python, un conjunto (set) es una colección desordenada de elementos 
#únicos. Los conjuntos son útiles cuando necesitas eliminar duplicados, 
#realizar operaciones matemáticas de teoría de conjuntos (como uniones 
#e intersecciones) o comprobar pertenencia.

miConjunto = {1, 2, 3, 4, 5}
print(miConjunto)

#para crear un conjunto vacio (diferente de un diccionario), usa:
conjuntoVacio = set()
print(conjuntoVacio)

