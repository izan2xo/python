#En Python, una tupla es una colección ordenada e inmutable de elementos.
#A diferencia de las listas, no puedes modificar una tupla después de 
#crearla (no se pueden agregar, eliminar ni cambiar elementos). 
#Sin embargo, hay funciones y métodos útiles para trabajar con tuplas. 
#A continuación, se presentan las principales funciones y métodos 
#relacionados con las tuplas.

miTupla = (1, 2, 3, 4, 5)
print(miTupla)

#las tuplas de un solo elemento se les pone una coma para evitar confuciones

tuplaConUnElemento = (1,)
print(tuplaConUnElemento)
