#En Python, un diccionario es una colección desordenada de pares clave-valor, 
#donde cada clave es única y se utiliza para acceder a su valor correspondiente. 
#Los diccionarios son muy útiles para almacenar datos estructurados y realizar 
#búsquedas rápidas.

miDiccionario = {
    "nombre": "Andres",
    "edad": 21,
    "ciudad": "Madrid"
}
print(miDiccionario)

#para crear un diccionario vacio
diccionarioVacio = {}
print(diccionarioVacio)