#Las estructuras condicionales sirven para permitir que un programa 
#tome decisiones y ejecute diferentes bloques de código dependiendo de 
#ciertas condiciones. Son esenciales para controlar el flujo del programa 
#y adaptarlo a diferentes escenarios o entradas, haciéndolo más dinámico e 
#interactivo.

#Las estructuras condicionales en Python son bloques de código que permiten 
#tomar decisiones dentro de un programa en función de si una condición lógica 
#es verdadera o falsa. Estas estructuras son fundamentales para controlar el 
#flujo del programa y ejecutar diferentes instrucciones según ciertas 
#condiciones.